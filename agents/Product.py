        self.prod_item_company = prod_item_company,
        self.prod_item_unit = prod_item_unit
        self.prod_item_quantity = prod_item_quantity
        self.prod_item_cost = prod_item_cost
        self.prod_item_delivered = prod_item_delivered
        self.prod_item_valid_until = prod_item_valid_until

